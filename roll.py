from .. import loader, utils
import random

@loader.tds
class RollModule(loader.Module):
    """–ú–æ–¥—É–ª—å –¥–ª—è –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —Å–ª—É—á–∞–π–Ω–æ–≥–æ —á–∏—Å–ª–∞ –≤ –∑–∞–¥–∞–Ω–Ω–æ–º –¥–∏–∞–ø–∞–∑–æ–Ω–µ."""

    strings = {
        "name": "/roll",
        "info_about_module": "–≠—Ç–æ—Ç –º–æ–¥—É–ª—å –ø–æ–∑–≤–æ–ª—è–µ—Ç –≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å —Å–ª—É—á–∞–π–Ω—ã–µ —á–∏—Å–ª–∞. –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—É: .roll <min>:<max>",
        "incorrect_format": "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —É–∫–∞–∂–∏—Ç–µ –¥–∏–∞–ø–∞–∑–æ–Ω –≤ —Ñ–æ—Ä–º–∞—Ç–µ <min>:<max>.",
        "min_max_error": "–ú–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –º–µ–Ω—å—à–µ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è.",
        "value_error": "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, —É–∫–∞–∂–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ —á–∏—Å–ª–æ–≤—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –≤ —Ñ–æ—Ä–º–∞—Ç–µ <min>:<max>."
    }

    def __init__(self):
        self.config = loader.ModuleConfig(
            "MT", "–í—ã –≤—ã–±—Ä–æ—Å–∏–ª–∏ {otv} ({diapos})",
            lambda: "–®–∞–±–ª–æ–Ω —Å–æ–æ–±—â–µ–Ω–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞.\n\n–ò—Å–ø–æ–ª—å–∑—É–π {diapos} –¥–ª—è –¥–∏–∞–ø–∞–∑–æ–Ω–∞ –∑–Ω–∞—á–µ–Ω–∏–π\n–ò–ø–æ–ª—å–∑—É–π {otv} –¥–ª—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞."
        )

    async def rollcmd(self, message):
        """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —Å–ª—É—á–∞–π–Ω–æ–µ —á–∏—Å–ª–æ –≤ —É–∫–∞–∑–∞–Ω–Ω–æ–º –¥–∏–∞–ø–∞–∑–æ–Ω–µ min:max.\n\nüíª developer [@belka_mod]"""
        args = utils.get_args_raw(message)

        if ':' not in args:
            await utils.answer(message, self.strings["incorrect_format"])
            return

        try:
            min_val, max_val = map(int, args.split(':'))
            if min_val >= max_val:
                await utils.answer(message, self.strings["min_max_error"])
                return
        except ValueError:
            await utils.answer(message, self.strings["value_error"])
            return

        random_number = random.randint(min_val, max_val)
        
        # –§–æ—Ä–º–∏—Ä—É–µ–º –æ—Ç–≤–µ—Ç —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —à–∞–±–ª–æ–Ω–∞ –∏–∑ –∫–æ–Ω—Ñ–∏–≥–∞
        result_message = self.config["MT"].format(diapos=f"{min_val}-{max_val}", otv=random_number)
        await utils.answer(message, result_message)


    async def setconf(self, message):
        """–ü–æ–∑–≤–æ–ª—è–µ—Ç –∏–∑–º–µ–Ω—è—Ç—å –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é —à–∞–±–ª–æ–Ω–∞."""
        args = utils.get_args_raw(message)
        if not args:
            await utils.answer(message, "–£–∫–∞–∂–∏—Ç–µ –Ω–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –¥–ª—è —à–∞–±–ª–æ–Ω–∞.")
            return

        self.config["MT"] = args
        await utils.answer(message, f"{self.strings['config_updated'].format(template=self.config['MT'])}")
